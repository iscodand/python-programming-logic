"""Faça um Programa que verifique se uma letra digitada é vogal ou consoante."""

letra = (input('Insira uma letra do alfabeto: '))

if letra == "A" or letra == "a" or letra == "B" or letra == "b" or letra == "C" or letra == "c" or letra == "D" or letra == "d" or letra == "E" or letra == "e":

    print('A letra inserida é uma Vogal.')

else:
    print('A letra inserida é uma consoante')
